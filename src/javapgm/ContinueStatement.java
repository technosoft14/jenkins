package javapgm;

public class ContinueStatement {
	public static void main(String[] args) {
			
	int choice=3;
	switch(choice) {
	case 1:
		System.out.println("one");
	break;
	case 2:
		System.out.println("Two");
	break;
	case 3:
		System.out.println("three");
	
	case 4:
		System.out.println("Four");
	break;
	
	default:
	System.out.println("Invalid");
	
	
	}


}
}




